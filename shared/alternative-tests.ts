export function getQuestionsByTestType(testType: string, count: number) {
  let questions: Question[] = [];
  
  switch (testType) {
    case "darkSide":
      questions = darkSideQuestions;
      break;
    case "cunningSide":
      questions = cunningSideQuestions;
      break;
    case "goodSide":
      questions = goodSideQuestions;
      break;
    case "mysteriousSide":
      questions = mysteriousSideQuestions;
      break;
    case "zodiac":
      questions = zodiacQuestions;
      break;
    default:
      return [];
  }
  
  const shuffled = [...questions].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, questions.length));
}
